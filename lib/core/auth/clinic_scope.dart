/// Мульти-клиничность (изоляция данных между клиниками).
///
/// [ClinicScope.current] — идентификатор клиники ТЕКУЩЕЙ сессии. Его
/// проставляет `AuthRepository` при входе (из `staff/{uid}.clinic_id`) и
/// очищает при выходе. ВСЕ репозитории:
///   • на запись штампуют поле `clinic_id: ClinicScope.current`;
///   • на чтение фильтруют `.where('clinic_id', isEqualTo: ClinicScope.current)`.
/// Так данные одной клиники никогда не видны другой. Правила Firestore
/// дублируют эту изоляцию на сервере (клиент — не граница безопасности).
///
/// Если [current] == null (не вошли / профиль без клиники) — запросы с фильтром
/// вернут пусто, а стамп запишет null (правила отклонят). Это безопасное
/// поведение по умолчанию: без назначенной клиники доступа к данным нет.
class ClinicScope {
  ClinicScope._();

  /// Активная клиника сессии. `null`, пока не выполнен вход или у сотрудника
  /// не назначена клиника.
  static String? current;

  /// Признак «платформенный администратор» текущей сессии (управляет клиниками
  /// и заводит им первого администратора; операционные данные всё равно
  /// ограничены его собственной клиникой [current]).
  static bool isPlatformAdmin = false;

  /// Сброс при выходе.
  static void clear() {
    current = null;
    isPlatformAdmin = false;
  }
}

/// Клиника по умолчанию: в неё мигрируются ВСЕ существующие данные при
/// включении мульти-клиничности (бэкфилл `clinic_id = kDefaultClinicId`).
/// Новые клиники получают сгенерированные id.
const String kDefaultClinicId = 'default';
